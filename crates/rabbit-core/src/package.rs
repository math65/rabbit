    #[test]
    fn parses_embedded_package_manifest() {
        let manifest = embedded_package_manifest();

        assert_eq!(manifest.schema_version, 1);
        assert_eq!(manifest.packages.len(), 9);
        assert!(
            manifest
                .packages
                .iter()
                .any(|package| package.id == PACKAGE_REAPER)
        );
        assert!(
            manifest
                .packages
                .iter()
                .any(|package| package.id == PACKAGE_OSARA)
        );
        assert!(
            manifest
                .packages
                .iter()
                .any(|package| package.id == PACKAGE_REAKONTROL)
        );
        let reakontrol = manifest
            .packages
            .iter()
            .find(|package| package.id == PACKAGE_REAKONTROL)
            .unwrap();
        assert_eq!(reakontrol.package_kind, PackageKind::UserPluginBinary);
        // ReaKontrol is data-driven via a `github_release` block.
        let github = reakontrol
            .github_release
            .as_ref()
            .expect("reakontrol uses a github_release block");
        assert_eq!(github.repo, "jcsteh/reaKontrol");
        assert!(matches!(github.release, GithubReleaseSelector::Latest));
        assert!(matches!(
            github.version_from,
            VersionSource::AssetName {
                strip_trailing_dot_segment: true
            }
        ));
        assert_eq!(github.artifact_kind, Some(GithubArtifactKind::Archive));
        assert_eq!(github.install_destination, InstallDestination::UserPlugins);
        assert_eq!(reakontrol.user_plugin_prefixes, vec!["reaper_kontrol"]);
        assert!(
            reakontrol
                .install_steps
                .contains(&InstallStep::CopyUserPluginBinary)
        );
        assert!(
            reakontrol
                .detectors
                .contains(&PackageDetector::UserPluginFile)
        );
        let reaper = manifest
            .packages
            .iter()
            .find(|package| package.id == PACKAGE_REAPER)
            .unwrap();
        assert_eq!(reaper.package_kind, PackageKind::ReaperApp);
        // REAPER's version is a data-driven HTML rule and its artifact is a
        // data-driven `http_artifact` page scrape.
        assert!(matches!(reaper.version, Some(VersionRule::Html { .. })));
        let reaper_http = reaper.http_artifact.as_ref().expect("reaper http_artifact");
        assert!(reaper_http.targets.iter().any(|target| matches!(
            (target.platform, target.artifact_kind),
            (SupportedPlatform::Windows, GithubArtifactKind::Installer)
        )));
        assert_eq!(reaper.backup_policy, BackupPolicy::None);
        assert!(
            reaper
                .install_steps
                .contains(&InstallStep::RunUpstreamInstaller)
        );
        let osara = manifest
            .packages
            .iter()
            .find(|package| package.id == PACKAGE_OSARA)
            .unwrap();
        assert_eq!(osara.package_kind, PackageKind::UserPluginBinary);
        assert!(matches!(osara.version, Some(VersionRule::Json { .. })));
        assert!(osara.http_artifact.is_some());
        assert_eq!(osara.backup_policy, BackupPolicy::BackupOverwrittenFiles);
        assert!(osara.detectors.contains(&PackageDetector::UserPluginFile));
        assert!(
            osara
                .install_steps
                .contains(&InstallStep::CopyUserPluginBinary)
        );
        // The per-package files were gathered at compile time and are
        // non-empty (build.rs picked them up).
        assert!(embedded_package_manifest_bytes() > 0);
        let jaws = manifest
            .packages
            .iter()
            .find(|package| package.id == PACKAGE_JAWS_SCRIPTS)
            .unwrap();
        assert_eq!(jaws.package_kind, PackageKind::ScreenReaderScripts);
        assert_eq!(jaws.supported_platforms, vec![SupportedPlatform::Windows]);
        // Manifest baseline is "not recommended"; the package escalates to
        // recommended-by-default via `recommended_when: jaws_installed` so
        // it lands in the desired set only on hosts with JAWS detected.
        assert!(!jaws.recommended);
        assert_eq!(jaws.recommended_when, Some(HostCapability::JawsInstalled));
        // JAWS is data-driven via an `hfs_listing` block (rejetto HFS folder),
        // the only package that isn't a web page or GitHub release.
        let jaws_hfs = jaws.hfs_listing.as_ref().expect("jaws uses hfs_listing");
        assert_eq!(jaws_hfs.base, "https://hoard.reaperaccessibility.com");
        assert!(jaws_hfs.folder.contains("JAWS%20Scripts%20by%20Snowman"));
        assert_eq!(jaws_hfs.artifact_kind, GithubArtifactKind::Installer);
        let surge = manifest
            .packages
            .iter()
            .find(|package| package.id == PACKAGE_SURGE_XT)
            .unwrap();
        assert_eq!(surge.package_kind, PackageKind::UserPluginBinary);
        assert_eq!(
            surge.supported_platforms,
            vec![SupportedPlatform::Windows, SupportedPlatform::Macos]
        );
        assert!(!surge.recommended);
        // Surge XT folds into the data-driven `github_release` engine for BOTH
        // version and artifact.
        let surge_github = surge
            .github_release
            .as_ref()
            .expect("surge uses a github_release block");
        assert_eq!(surge_github.repo, "surge-synthesizer/surge");
        assert!(matches!(
            &surge_github.release,
            GithubReleaseSelector::Tag(tag) if tag == "Nightly"
        ));
        assert!(surge_github.artifact_kind.is_none());
        assert!(surge_github.assets.iter().any(|selector| matches!(
            (selector.platform, selector.artifact_kind),
            (
                SupportedPlatform::Windows,
                Some(GithubArtifactKind::Installer)
            )
        )));
        assert!(
            surge
                .install_steps
                .contains(&InstallStep::RunUpstreamInstaller)
        );
        assert!(surge.detectors.contains(&PackageDetector::RabbitReceipt));
        assert!(surge.detectors.contains(&PackageDetector::SurgeVendorFiles));
        assert!(surge.user_plugin_prefixes.is_empty());
    }

