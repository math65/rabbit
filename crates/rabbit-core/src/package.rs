/// Data-driven definition of a package distributed through GitHub releases,
/// read by BOTH the version side ([`crate::latest`]) and the download side
/// ([`crate::artifact`]). When a package carries one of these, adding or
/// maintaining it needs only this manifest block — no per-package Rust. It
/// is the generalization of the hand-written ReaKontrol/app2clap snapshot
/// parsers + resolvers into one parameterized engine.
///
/// A package sets exactly one artifact source — `github_release`,
/// `http_artifact`, or `hfs_listing` — enforced by [`validate_package_spec`].
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct GithubReleaseSpec {
    /// `owner/name`, e.g. `jcsteh/app2clap`.
    pub repo: String,
    /// Which release to read: `/releases/latest` or `/releases/tags/<tag>`.
    pub release: GithubReleaseSelector,
    /// How to derive the version (from the tag, or from an asset filename).
    pub version_from: VersionSource,
    /// Per-platform (optionally per-arch) asset matchers used to pick the
    /// download. The version side ignores this — it reads `version_from`.
    pub assets: Vec<AssetSelector>,
    /// Spec-wide fallback for how the downloaded asset is handled. Optional:
    /// a package whose platforms need *different* kinds (Surge XT —
    /// `Installer` on Windows, `DiskImage` on macOS) leaves this `None` and
    /// sets [`AssetSelector::artifact_kind`] per asset instead. The resolved
    /// kind is `selector.artifact_kind.or(spec.artifact_kind)`; the load-time
    /// validator rejects a spec where neither is set for some asset.
    #[serde(default)]
    pub artifact_kind: Option<GithubArtifactKind>,
    /// Where the resolved file installs. Defaults to `UserPlugins`.
    #[serde(default)]
    pub install_destination: InstallDestination,
}

/// Which GitHub release a [`GithubReleaseSpec`] reads. Serializes as the
/// bare string `"latest"` or as `{ "tag": "<name>" }`.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum GithubReleaseSelector {
    /// `GET /repos/<repo>/releases/latest`.
    Latest,
    /// `GET /repos/<repo>/releases/tags/<tag>` — a fixed or rolling tag
    /// (e.g. `snapshots`). Assets are sorted by version; release date/order
    /// is never trusted.
    Tag(String),
}

/// How a [`GithubReleaseSpec`] derives the package version. Serializes as
/// `{ "tag_name": { … } }` or `{ "asset_name": { … } }`.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum VersionSource {
    /// The release's `tag_name`, optionally with a leading `v` stripped
    /// (ReaPack's `v1.2.6` → `1.2.6`).
    TagName {
        #[serde(default)]
        strip_v_prefix: bool,
    },
    /// The version embedded in the highest-sorting asset filename. The
    /// prefix/suffix to strip come from the matching [`AssetSelector`] (so a
    /// multi-platform package like ReaKontrol, whose Windows and macOS
    /// assets carry different prefixes, needs no per-platform version rule);
    /// `strip_trailing_dot_segment` additionally drops a trailing
    /// `.<segment>` (the short commit hash in
    /// `app2clap_2026.5.17.34.b6f558cf.zip`).
    AssetName {
        #[serde(default)]
        strip_trailing_dot_segment: bool,
    },
}

/// A per-platform (and optionally per-arch) rule for selecting the release
/// asset to download. Exactly one of (`name_prefix` + `name_suffix`) or
/// `exact_name` must be set — enforced at manifest load.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct AssetSelector {
    pub platform: SupportedPlatform,
    /// `None` matches any host arch and the resolved descriptor reports
    /// `Architecture::Universal`; `Some` pins both the match and the
    /// reported architecture (ReaPack's per-slice DLLs).
    #[serde(default)]
    pub arch: Option<Architecture>,
    #[serde(default)]
    pub name_prefix: Option<String>,
    #[serde(default)]
    pub name_suffix: Option<String>,
    #[serde(default)]
    pub exact_name: Option<String>,
    /// Per-asset override of [`GithubReleaseSpec::artifact_kind`]. Lets a
    /// multi-platform package ship different kinds per slice (Surge XT's
    /// Windows `Installer` vs macOS `DiskImage`). `None` falls back to the
    /// spec-wide kind.
    #[serde(default)]
    pub artifact_kind: Option<GithubArtifactKind>,
}

impl AssetSelector {
    /// Whether `asset_name` satisfies this selector.
    pub fn matches_asset(&self, asset_name: &str) -> bool {
        if let Some(exact) = &self.exact_name {
            return asset_name == exact;
        }
        let prefix_ok = self
            .name_prefix
            .as_ref()
            .is_none_or(|prefix| asset_name.starts_with(prefix));
        let suffix_ok = self
            .name_suffix
            .as_ref()
            .is_none_or(|suffix| asset_name.ends_with(suffix));
        prefix_ok && suffix_ok
    }
}

/// How a downloaded artifact (a GitHub asset OR a scraped/fixed download) is
/// handled. Maps 1:1 onto [`crate::artifact::ArtifactKind`]. Shared by both
/// the [`GithubReleaseSpec`] path and the non-GitHub [`HttpArtifactSpec`] path.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum GithubArtifactKind {
    /// A `.zip` from which the matching plugin file is extracted.
    Archive,
    /// A bare plugin file dropped in place (ReaPack's `.dll`/`.dylib`).
    ExtensionBinary,
    /// A vendor `.exe`/`.pkg` installer run by `run_upstream_installer`
    /// (REAPER, SWS, OSARA-win, Surge XT-win).
    Installer,
    /// A macOS `.dmg` (REAPER, SWS-mac, Surge XT-mac).
    DiskImage,
    /// A `.7z` archive (FFmpeg shared builds). Extracted by the dedicated 7z
    /// path since the `zip` crate can't read it.
    SevenZipArchive,
}

/// Where a data-driven package installs its file.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize, Default)]
#[serde(rename_all = "snake_case")]
pub enum InstallDestination {
    /// `<resource>/UserPlugins/` — the default for REAPER extensions.
    #[default]
    UserPlugins,
    /// `%LOCALAPPDATA%\Programs\Common\CLAP` — the per-user CLAP folder
    /// (app2clap), outside any REAPER resource path.
    WindowsClapDir,
}

/// Data-driven artifact (download) resolution for packages that are NOT a
/// single GitHub release: REAPER (reaper.fm scrape), OSARA (snapshot-index
/// scrape), SWS (home-page scrape) and FFmpeg (per-arch dual source). Read
/// only by [`crate::artifact`]; the version side keeps using [`VersionRule`]
/// and the resolved version is stamped onto the descriptor.
///
/// Every target is keyed by `(platform, match_arches)`. The first target
/// whose platform matches and whose `match_arches` contains the (already
/// canonicalized) dispatch arch wins. No matching target yields
/// `NoArtifactFound` — this is how SWS rejects Windows-on-ARM and FFmpeg
/// rejects x86/macOS, with no per-package Rust. A package sets EXACTLY ONE of
/// `github_release` / `http_artifact` / `hfs_listing` (enforced by the
/// load-time validator).
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct HttpArtifactSpec {
    pub targets: Vec<HttpArtifactTarget>,
}

/// One `(platform, arches)` → download rule.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct HttpArtifactTarget {
    pub platform: SupportedPlatform,
    /// Input dispatch arches (post-`canonicalize_dispatch_arch`) this target
    /// serves. The arch-collapse fan-in lives here: REAPER-win lists
    /// `["x64","universal","unknown"]` on its x64 target. `universal`/`unknown`
    /// are accepted belt-and-suspenders even though canonicalization usually
    /// rewrites them to a concrete host slice first.
    pub match_arches: Vec<Architecture>,
    /// Architecture stamped on the resulting descriptor (REWRITES arch:
    /// win x64-target → `x64`, mac universal-target → `universal`, osara →
    /// `universal`, ffmpeg-arm64 → `arm64`). Independent of `match_arches`.
    pub report_arch: Architecture,
    pub artifact_kind: GithubArtifactKind,
    /// How the download URL + file name are produced.
    pub source: HttpArtifactSource,
}

/// The shapes the snowflakes need. Externally tagged (`serde` tag = variant).
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum HttpArtifactSource {
    /// Scrape an HTML page for the FIRST `<a href>` whose value matches.
    /// Covers REAPER, OSARA, SWS. The matched href becomes the download URL
    /// via `absolute_url(base_url, href)`; the file name is its basename. The
    /// version comes from the package's [`VersionRule`] and is stored on the
    /// descriptor only (never interpolated).
    ScrapeHref {
        page_url: String,
        base_url: String,
        /// Anchored predicate over the href. `contains` is an AND-list;
        /// `not_contains` disambiguates REAPER's x86 `-install.exe` (a
        /// substring of the x64/arm64 hrefs) so matching is order-INDEPENDENT
        /// instead of relying on document order; `ends_with` pins the
        /// extension.
        #[serde(rename = "match")]
        href_match: HrefMatch,
    },
    /// A fixed download URL (no version interpolation). Covers FFmpeg x64
    /// (Gyan redirector). `file_name` overrides the URL basename so the cache
    /// layout stays stable if the redirector path changes.
    FixedUrl {
        url: String,
        #[serde(default)]
        file_name: Option<String>,
    },
    /// Highest *stable* GitHub release whose major == `supported_major`, then
    /// the asset matching `asset`. Covers FFmpeg arm64 (tordona). The version
    /// is the picked release's, NOT the package `VersionRule`. Distinct from
    /// `github_release` because tags are plain `MAJOR.MINOR.PATCH` and
    /// "newest within a pinned major" isn't expressible by
    /// [`GithubReleaseSelector`].
    GithubReleaseMaxMajor {
        /// `owner/name`, e.g. `tordona/ffmpeg-win-arm64`.
        repo: String,
        /// Major-version gate; must equal `FFMPEG_SUPPORTED_MAJOR`.
        supported_major: u64,
        asset: AssetMatch,
    },
}

/// Anchored href predicate for [`HttpArtifactSource::ScrapeHref`].
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct HrefMatch {
    #[serde(default)]
    pub contains: Vec<String>,
    #[serde(default)]
    pub not_contains: Vec<String>,
    #[serde(default)]
    pub ends_with: Option<String>,
}

impl HrefMatch {
    /// Whether `href` satisfies every clause (all `contains`, no
    /// `not_contains`, optional `ends_with`).
    pub fn matches(&self, href: &str) -> bool {
        self.contains.iter().all(|needle| href.contains(needle))
            && self
                .not_contains
                .iter()
                .all(|needle| !href.contains(needle))
            && self.ends_with.as_ref().is_none_or(|s| href.ends_with(s))
    }
}

/// Asset predicate for [`HttpArtifactSource::GithubReleaseMaxMajor`].
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct AssetMatch {
    #[serde(default)]
    pub contains: Vec<String>,
    #[serde(default)]
    pub ends_with: Option<String>,
}

impl AssetMatch {
    pub fn matches(&self, name: &str) -> bool {
        self.contains.iter().all(|needle| name.contains(needle))
            && self.ends_with.as_ref().is_none_or(|s| name.ends_with(s))
    }
}

/// Data-driven definition of a package distributed through a rejetto HFS
/// folder listing (`hoard.reaperaccessibility.com`). Like [`GithubReleaseSpec`]
/// it drives BOTH the version side ([`crate::latest`]) and the download side
/// ([`crate::artifact`]) from one source: the engine reads the folder listing,
/// picks the highest-version installer, and uses it for both. Covers JAWS-for-
/// REAPER scripts — the one package that isn't a web page or GitHub release.
/// The HFS `get_file_list` protocol and the `.exe` highest-version pick stay as
/// engine helpers selected by this block (the same "spec selects an irreducible
/// helper" pattern as [`HttpArtifactSource::GithubReleaseMaxMajor`]).
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct HfsListingSpec {
    /// rejetto HFS server root, e.g. `https://hoard.reaperaccessibility.com`.
    pub base: String,
    /// URL-encoded folder path under the root whose listing is read.
    pub folder: String,
    /// How the picked file is handled (JAWS ships an `Installer` `.exe`).
    pub artifact_kind: GithubArtifactKind,
}

/// Data-driven latest-version discovery for packages that don't come from a
/// GitHub release. Each variant fetches a URL and extracts a version, so a
/// snowflake's version side is manifest data rather than a per-package Rust
/// parser. Serializes as `{ "html": { … } }` / `{ "json": { … } }` /
/// `{ "plain_text": { … } }`.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum VersionRule {
    /// Fetch `url`, match `pattern` (a regex) against the body, and build the
    /// version from the capture groups via `format` (default `"{1}"`, i.e.
    /// the first group). Covers HTML scrapes like REAPER's
    /// `Version ([0-9.]+)` and SWS's two-group `base` + `#build`
    /// (`format: "{1}.{2}"`).
    Html {
        url: String,
        pattern: String,
        #[serde(default = "default_capture_format")]
        format: String,
    },
    /// Fetch `url` (JSON) and read the string at `pointer` (an RFC 6901 JSON
    /// pointer, e.g. `/version`). Covers OSARA's `update.json`.
    Json { url: String, pointer: String },
    /// Fetch `url` and parse the trimmed body as a version. Covers FFmpeg's
    /// Gyan `*.ver` plain-text endpoint.
    PlainText { url: String },
}

fn default_capture_format() -> String {
    "{1}".to_string()
}

/// A detection probe. Listed in `detectors` per package and run by the
/// detection engine in order, so the manifest declares both *which* probes
/// apply and their fallback order. `RabbitReceipt` (authoritative) and
/// `UserPluginFile` (file presence) are special bookends; the rest are
/// version probes / out-of-tree detectors, some carrying their parameters
/// as data (registry name, scan pattern), some selecting a bespoke engine
/// helper whose domain logic is irreducible.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum PackageDetector {
    /// RABBIT's own install receipt — authoritative version, checked first.
    RabbitReceipt,
    /// A matching file's presence in the package's install destination.
    UserPluginFile,
    /// The detected file's PE / Mach-O version metadata.
    FileVersionMetadata,
    /// The ReaPack local registry's recorded version for the file.
    ReapackRegistry,
    /// The Windows uninstall-registry `DisplayVersion` for `display_name`
    /// (e.g. OSARA's installer entry, shown in Programs and Features).
    UninstallDisplayVersion { display_name: String },
    /// A best-effort scan of the detected binary for a version string matched
    /// by `pattern` (regex, capture group 1), reported as `label` with
    /// `note`. Generalizes the OSARA / ReaKontrol / SWS / ReaPack embedded-
    /// version-string scanners (each just a different anchored pattern).
    BinaryVersionString {
        pattern: String,
        label: String,
        note: String,
    },
    /// FFmpeg's bespoke 3-probe detector (`ffmpeg.exe` StringFileInfo →
    /// binary banner scan → `avformat-XX` filename → libavformat-major map).
    /// The map (`lib_major - 54`) and probe orchestration stay in the engine.
    FfmpegLibavformat,
    /// JAWS-for-REAPER scripts: follow the Programs-and-Features uninstall
    /// key to the vendor `Uninstall.exe` and read its FileVersion resource.
    JawsUninstallExe,
    /// Surge XT: the Inno Setup uninstall `DisplayVersion` plus the system
    /// VST3 bundle's file metadata.
    SurgeVendorFiles,
}

